            <h4>Where Do I Begin?</h4>
            <p>The first step is to determine what your site is going to be about, and who your audience is. You can do this by asking yourself the following questions:</p>
            <h5>Determining business goals</h5>
            <ul>
                <li>What would you like your web presence to achieve?</li>
                <li>What kind of objectives do you have for your site?
                    <ul>
                        <li>Are you focused on conversions / getting people to buy your product(s)?</li>
                        <li>Do you want your site to be informational / educational for others?</li>
                        <li>Are you trying to gather information from your visitors for some other purpose?</li>
                    </ul></li>
            </ul>
            <h5>Determining target audience</h5>
            <ul>
                <li>Who is your product/service geared for?
                    <ul>
                        <li>Age?</li>
                        <li>Occupation?</li>
                        <li>Education level?</li>
                        <li>Geographic location?</li>
                    </ul>
                </li>
            </ul>
            <p>These are just a few of the questions you should be asking yourself. By answering these questions, you can further determine what kind of <a class="link" data-target="content" data-section="planning" title="Visit the content page to learn the best practices for generating content">content</a> you'll need to create. Also they help determine the best way to reach your target audience.</p>

            <h4>Yeah, But What About Simple Redesigns?</h4>
            <p>There's no such thing. Even if the site you're building is just a refresher of a site that already exists, you should be going through the planning process to ensure that the new site meets all of the business's needs. The target audience may have changed, there may be new accessibility issues you need to address, new features to address needs not currently being met, or content may have changed and SEO is no longer accurate.</p>
            <h5>Heuristic analyses and task flows</h5>
            <p>These are unique parts of the planning process that need to be implemented in a "simple redesign."</p>
            <p>A heuristic analysis is when usability tests are conducted by completing specific tasks on the website. These analyses will reveal problem areas that can then be addressed, giving the user a better experience.</p>
            <p>Task flows consist of determining what the primary tasks are on the existing site, documenting how they are presently achieved, and pinpointing any potential improvement areas.</p>
            <h5>Never think that a redesign should be "simple"</h5>
            <p>So much more goes into a redesign than just changing the color palette and layout. And tackling it head-on with a full plan will allow for accurate quotes/estimates, ensuring that you don't short-change yourself.</p>
