        <h3>Accessibility is Key</h3>
        <p>Make sure your site is accessible for all visitors. Not just mobile site users - but for those who are blind or have other impairments. Here are some do's and don'ts:</p>
        <ul>
            <li><strong>Don't</strong> use "click here..." for links.</li>
            <li><strong>Do</strong> make the anchor text descriptive and use a title attribute if it's not obvious where the link will lead in the current context.</li><br>

            <li><strong>Do</strong> add alt tags to every image. These assist screen readers in describing images to blind visitors.</li>
            <li><strong>Don't</strong> create generic alt tags like "image", "picture", "graphic", or the filename.</li>
            <li><strong>Do</strong> create descriptive alt tags that clearly describe what the image is of.</li><br>

            <li><strong>Don't</strong> auto-play videos/audio files on your site. </li>
            <li><strong>Do</strong> allow users to choose to auto-play media on your site. </li><br>

            <li><strong>Do</strong> add captions and transcripts for media such as audio and video.</li><br>
            <li><strong>Do</strong> ensure that users can easily adjust sound levels.</li><br>
            <li><strong>Do</strong> implement descriptions for charts and diagrams.</li><br>

            <li><strong>Don't</strong> incorporate colors that have low contrast or are difficult to read.</li>
            <li><strong>Do</strong> use colors that are easy to see with high contrast for visitors who have vision impairments.</li><br>

            <li><strong>Don't</strong> use tiny text sizes or hard to read font-faces.</li>
            <li><strong>Do</strong> use text that is readable and appropriate for your audience that can be resized.</li><br>

            <li><strong>Don't</strong> incorporate any flashing media or colors that could induce seizures.</li><br>

            <li><strong>Do</strong> ensure that all functions can be controlled by either keyboard or mouse.</li><br>

            <li><strong>Don't</strong> create lots of modals or pop ups for collecting information from your users.</li>
            <li><strong>Do</strong> create easy to use forms as regular pages so your visitors can choose to enter info.</li><br><br>

            <li><strong>Don't</strong> block users from viewing your content just to force them to like your social media page.</li>
            <li><strong>Do</strong> encourage interaction with Twitter and Facebook integration on your site in a non-intrusive way.</li>
        </ul>

