            <p>This is a <strong>crucial</strong> part of the process of building an effective website. What is easy-to-use for you, may not be for the average user who doesn't know anything about building websites.</p>
            <p>As such it's incredibly important that toward the end of the development stage, you begin usability testing.</p>

            <p>Test your site in every browser, resolution, etc that you can get your hands on. iPhones, Android and Windows phones, Windows and Mac machines, Chrome, Firefox, Safari, and of course (and unfortunately) Internet Explorer/Edge.</p>
            <p>Gather as many people as you can on as many different devices as you can and have them explore the site and give you feedback. Get people who are not close to the project and can act objectively as standard users so you can gather valuable feedback.</p>
            <p>What is easy for you as the designer or developer of the site, may not be easy for a standard user.</p>
            <p>Give your testers specific tasks to achieve on the site and determine how easy or difficult it was for them to complete. This will help guide you onto any revisions that you may need to make to ensure that your site is effective.</p>

