            <h1>Follow Up</h1>
            <h2>Don't let your site become obsolete!</h2>
            <p>It's so incredibly important to revisit the site at least quarterly to ensure that content is up to date and SEO information is accurate. You don't want to put all of this time and energy into creating a web presence just to let it grow dated and obsolete.</p>

            <h3>Maintaining Your Reputation</h3>
            <p>If someone searches out sites built by you and finds horribly dated or broken websites, that reflects poorly on the brand you've been working so hard to develop for yourself. So while you may not want to revisit projects you've already completed, you're going to have to decide which is more important to you - some time spent vs losing street cred. If it's one of your own sites, give it a face lift and refresh some content to keep it relevant.</p>

            <h3>Maintaining Relationships</h3>
            <p>If you built the site for a client, check in with them! Just drop a quick email to see how it's going. Ask if they are still happy with their finished site, and if they are having any problems or have questions or concerns. This will not only ensure that your clients are happy and more likely to stay loyal to you, but they will be more likely to recommend you to others which is a <strong>huge</strong> bonus for you! Plus there's the chance for you to up-sell with a redesign or content changes and gain a little extra work for yourself.</p>

